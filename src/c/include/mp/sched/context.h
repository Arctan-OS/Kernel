#ifndef ARC_MP_SCHED_CONTEXT_H
#define ARC_MP_SCHED_CONTEXT_H

#include <stdint.h>

struct ARC_Registers {
	uint64_t rax;
	uint64_t rbx;
	uint64_t rcx;
	uint64_t rdx;
	uint64_t rsi;
	uint64_t rdi;
	uint64_t rsp;
	uint64_t rbp;
	uint64_t rip;
	uint64_t r8;
	uint64_t r9;
	uint64_t r10;
	uint64_t r11;
	uint64_t r12;
	uint64_t r13;
	uint64_t r14;
	uint64_t r15;
	uint32_t flags;
	// Bit | Description
	// 0   | "Dirty", 1: signals external modification of register state,
	//                   cleared once changes have been accepted
}__attribute__((packed));

#endif
